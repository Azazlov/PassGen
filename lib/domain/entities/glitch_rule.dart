/// Правила глитчирования текста в пароль
enum GlitchRule {
  /// Смещение символов по фиксированному алфавиту
  shiftChars,

  /// Подстановка визуально схожих символов
  visualSubstitute,

  /// Детерминированная «соль» из хэша входного текста
  derivedSalt,

  /// Инверсия регистра по позициям
  invertCase,

  /// Базовый leet-спик
  leetSpeak,
}
